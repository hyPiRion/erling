-module(erling_plugin_sup).

-behaviour(supervisor).
%%------------------------------------------------------------------------------
%% Include files
%%------------------------------------------------------------------------------

%%------------------------------------------------------------------------------
%% External exports
%%------------------------------------------------------------------------------
-export([
         start_link/0
        ]).

%%------------------------------------------------------------------------------
%% Internal exports
%%------------------------------------------------------------------------------
-export([
         init/1
        ]).

%%------------------------------------------------------------------------------
%% Macros
%%------------------------------------------------------------------------------
-define(SERVER, ?MODULE).

%%------------------------------------------------------------------------------
%% Records
%%------------------------------------------------------------------------------

%%==============================================================================
%% External functions
%%==============================================================================
%%------------------------------------------------------------------------------
%% Function: start_link/0
%% Description: Starts the supervisor
%%------------------------------------------------------------------------------
start_link() ->
    supervisor:start_link({local, ?SERVER}, ?MODULE, []).

%%==============================================================================
%% Server functions
%%==============================================================================
%%------------------------------------------------------------------------------
%% Func: init/1
%% Returns: {ok,  {SupFlags,  [ChildSpec]}} |
%%          ignore                          |
%%          {error, Reason}
%%------------------------------------------------------------------------------
init([]) ->
    {ok, {{simple_one_for_one, 0, 1},
          [{erling_plugin,
            {erlang, apply, []},
            permanent, 5000, worker, []}]}}.

